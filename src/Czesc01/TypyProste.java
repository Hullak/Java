package Czesc01;

public class TypyProste {
    // liczby stałoprzecinkowe
    byte byteNumber = 127; // 1 bajt -128 do 127
    short shortNumber = 32689; // 2 bajty -32768 do 32767
    int intNumber = 32768999;  // 4 bajty -2 147 483 648 do 2 147 483 647
    long longNumber = 2148000L;  // 8 bajtów -2^63 do (2^63) -1

    // liczby zmiennoprzecinkowe
    float floatNumber = 4.99934F; // 4 bajty - max ok 6-7 liczb po przecinki
    double doubleNumber = 3.9999999999999999999999; // 8 bajtów 15 cyfr po przecinku

    // wartość logiczna
    boolean prawda = true;
    boolean falsz = false;

    // pojedynczy znak
    char letter = 'A';
}
